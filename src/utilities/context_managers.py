# /////////////////////////////////////////////////////////////////////////////
# UTILITY MODULE - CONTAINS CONTEXT MANAGERS
# /////////////////////////////////////////////////////////////////////////////

import os
from contextlib import contextmanager


# CONTEXT MANAGER TO TEMPORARILY CHANGE THE CURRENT WORKING DIRECTORY
# /////////////////////////////////////////////////////////////////////////////
@contextmanager
def directory(path):
    try:
        cwd = os.getcwd()
        os.chdir(path)
        yield
    finally:
        os.chdir(cwd)